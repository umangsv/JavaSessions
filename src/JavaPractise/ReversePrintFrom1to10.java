package JavaPractise;


public class ReversePrintFrom1to10 {

	public static void main(String[] args) {
	
		// for loop 
		
		for (int i = 10; i > 0; i--) {
			System.out.println("Let's print no.s in reverse " + i);
		}
		
		
		// while loop	
		int i = 10;		
		while (i>0) {
			System.out.println(" Let's print the no in reverse " + i);
			i--;
		}
		
		
		// do while loop
		
		int j=10;
		
		do {
			
			System.out.println("Let's print the no.s in reverse " + j );
			j--;
			
		} while (j>0);
		
		
		
	}

}
